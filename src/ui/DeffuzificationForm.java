    public static  String[][] convertToString(double[][] centroid) {
        String [][] result = new String[centroid.length][centroid[0].length];
        for (int i = 0; i < result.length; i++) {
            for (int j = 0; j < result[i].length; j++) {
                result[i][j] = centroid[i][j]+"";
            }
        }
        return result;
    }

    public static double[][] transpose(double[][] centroid) {
        double [][] result = new double[centroid[0].length][centroid.length];
        for (int i = 0; i < centroid.length; i++) {
            for (int j = 0; j < centroid[i].length; j++) {
                result[j][i] = YagersMinMax.approx(centroid[i][j],4);
            }
        }
        return result;
    }
