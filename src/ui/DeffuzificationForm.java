    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String filename = "";
        if (!(filename = filePath.getText()).equals("")) {
            try {
                String delimiter = this.delimiter.getText();
                if (delimiter.equals("")) {
                    JOptionPane.showMessageDialog(this, "specify the delimiter for each institution", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                double [][] centroid = null;
                int selectedIndex = fuzzyType.getSelectedIndex();
                if (selectedIndex ==0) {
                    Deffuzification def= new Deffuzification(filename, delimiter);
                    centroid = def.initiateProcess();
//                    CompensatoryAnd.centroidDef =centroid;
                }
                else{
                    Type2Deffuzification def = new Type2Deffuzification(filename, delimiter);
                    centroid =def.initProcess();
//                    Type2CompensatoryAnd.centroidDef =centroid;
                }
                String [] columns = new String[centroid.length];
                if (!institutionNames.getText().equals("")) {
                    columns = institutionNames.getText().split(",");
                }
                
                centroid = transpose(centroid);
                String [][] stringRep = convertToString(centroid);
                DefaultTableModel model = new DefaultTableModel(stringRep, columns);
                centroidTable.setModel(model);
            } catch (Exception ex) {
                Logger.getLogger(DeffuzificationForm.class.getName()).log(Level.SEVERE, null, ex);
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

